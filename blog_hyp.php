<p>Hypoxia refers to low oxygen conditions. Normally, <b style="color:red;">20.9% of the gas in the atmosphere is oxygen</b>. The partial pressure of oxygen in the atmosphere is 20.9% of the total barometric pressure. In water however, oxygen levels are much lower, approximately 1%, and fluctuate locally depending on the presence of photosynthetic organisms and relative distance to the surface .</p>

<p>Decrease in oxygen in the air can result in cyanosis, a blue discoloration of the skin. If hypoxia is very severe, a tissue may eventually become gangrenous. Extreme pain may also be felt at or around the site. Tissue hypoxia from low oxygen delivery may be due to low haemoglobin concentration (anaemic hypoxia), low cardiac output (stagnant hypoxia) or low haemoglobin saturation (hypoxic hypoxia). </p>

<p>Breathing as little as one or two breaths of air containing too little oxygen can have serious and immediate effects, including unconsciousness. Because there are no warning signs of reduced oxygen concentrations, these environments are extremely dangerous.</p>

<p style="color:red;"><b>Effects of exposure to low oxygen concentrations can include giddiness, mental confusion, loss of judgment, loss of coordination, weakness, nausea, fainting, loss of consciousness and death</b><p/>

<p>The immediate effects of low oxygen environments are due to our body’s oxygen transport system. </p>

<p>Blood absorbs oxygen from the air in our lungs to fuel the cells in our bodies. The brain is the body organ most sensitive to the lack of oxygen. </p>
<p>Within have seconds after inhaling only a few breaths of oxygen- free gas, there is a rapid drop in the oxygen concentration of the blood. Mental failure and coma follow a few seconds later. Symptoms or warnings are generally absent, but even if present, the loss of mental competence, weakness, loss of coordination, or fainting prevents victims from helping themselves or even summoning help. Death follows in just two to four minutes. </p>
