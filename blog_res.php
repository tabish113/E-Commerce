<p>A person breathes <b style="color:red;">7 or 8 liters</b> of air per minute which means about 11000 liters of air per day. Air is about 20% oxygen. But when we exhale, our breath is about 15% oxygen. So we consumed about 5%. Therefore, a person uses about 550 liters of pure oxygen each day.
In a year a person consumes <b style="color:red;">2,00,750 liters.</b></p>
<h3>Oxygen Intake by a Car</h3>
<p>The internal combustion engine in most cars burns gasoline. To do the burning, an engine needs oxygen, and the oxygen comes from the air all around us. The engine can burn no more gas than the amount of oxygen allows. Any extra fuel would come out of the exhaust pipe unburned.
</p>


