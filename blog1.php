<h2>India is one of the riskiest countries in the world to breathe.</h2>

<p>The air we breathe has a very exact chemical composition; <b style="color:red;">99 percent</b> of it is made up of
nitrogen, oxygen, water vapour and inert gases. Air pollution occurs when things that aren&#39;t
normally there are added to the air. A common type of air pollution happens when people
release particles into the air from burning fuels. This pollution looks like soot, containing
millions of tiny particles, floating in the air.</p>



<p>Another common type of air pollution is dangerous gases, such as sulphur dioxide, carbon
monoxide, nitrogen oxides and chemical vapours. These can take part in further chemical
reactions once they are in the atmosphere, creating acid rain and smog. Other sources of
air pollution can come from within buildings, such as second hand smoke. </p>

<p>Finally, air pollution can take the form of greenhouse gases, such as carbon dioxide or
sulphur dioxide, which are warming the planet through the greenhouse effect. The
greenhouse effect is when gases absorb the infrared radiation that is released from the
Earth, preventing the heat from escaping. This is a natural process that keeps our
atmosphere warm. If too many gases are introduced into the atmosphere, though, more
heat is trapped and this can make the planet artificially warm.</p>


<p>According to a study, Air pollution kills more than <b style="color:red;">2 million</b> people each year. The effects
of air pollution on human health can vary widely depending on the pollutant. If the pollutant
is highly toxic, the effects on health can be widespread and severe. An irritant <b style="color:red;">(e.g. particulates less than 10 micrometers)</b> may cause respiratory illnesses, cardiovascular disease and increases in asthma. &quot;The very young, the old and those with vulnerable
immune systems are most at risk from air pollution.</p>

<p>The air pollutant may be carcinogenic <b>(e.g. some volatile organic compounds)</b> or
biologically active (e.g. some viruses) or radioactive (e.g. radon). Other air pollutants like
carbon dioxide have an indirect impact on human health through climate change.</p>


