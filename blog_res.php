<h3>IMPORTANCE OF INDOOR PLANTS : </h3>

<p> * As per NASA`s Clean Air Study found that oxygen producing plants can be used to both produce oxygen as well as remove common harmful chemicals from the air and break them down into harmless organic byproducts into the soil, which the plants then use as food.</p>

<p> * The major benefit of adding oxygen producing plants to your living and work space is an increase of productivity due to the maintenance of healthy oxygen levels in the blood reduce the risk of eye irritation headaches, respiratory problems long impairments and asthma and also boost energy and productivity.
