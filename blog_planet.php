<h4>Our planet</h4>
<p><b style="color:red;">Our Planet “EARTH”is one Special Planet because it has life.</b> It has liquid water, climate
that shelters it from the worst of the sun&#39;s rays.</p>

<p><b style="color:red;">Climate:</b> The climate is one of the Fundamental life support system of Earth. Climate is different from weather. </p></br>

<p> <b style="color:red;">* The Atmosphere –</b> It is the air we inhale or exhale</p></br>
<p> <b style="color:red;">* The Hydrosphere-</b> it is the water on the Planet</p></br>

<p> <b style="color:red;">* The Biosphere-</b>  It is the animals, plants and trees. </p></br>


<p>The human revolution and human activities thereon have impacted each one of the above
components of the Climate. The emission of Green house gases modifies our atmosphere.
Excessive use of water and lack of water harvesting have modifies the Hyrosphere.
Increase in Green House Gases, Ozone depletion, deforestation modified the Cryoshpere .
Increase in population and pollution level modifies the biosphere
</p>

<p>As per Eliot Spitzer “Global warming threatens our health, our economy, our natural resources and our children`s future. It is clear we must act”
</p>

<h4>To heal the environment is full of challenge and the first step to change is to aware. </h4>

<p>The amount of oxygen in our atmosphere has been rapidly decreasing, due to the excess of carbon dioxide. As per the studies only two hundred years ago, there was 38 percent oxygen and 1 percent carbon dioxide in the air. Currently, the air in most places consists of 18 percent oxygen and 25 percent carbon dioxide.</p> 

<p>In some big cities of India, the oxygen levels have dropped below the required level Our atmosphere simply doesn’t provide us with as much oxygen as we need in order to function at our optimal levels. </p>

<p>When there is a high level of oxygen in an environment, viruses, amoebas, parasites, fungus, or bacteria can’t exist. These hostile organisms are responsible for the development of disease and health issues such as arthritis, ulcers, and diabetes. </p>

<p>Thus, lack of proper oxygen levels in our body leads to the development of potentially serious health problems.</p>

<p><b style="color:red;">GLOBAL WARMING :</b> Carbon dioxide (CO2) and other greenhouse gases act like a blanket, absorbing IR radiation and preventing it from escaping into outer space. The net effect is the gradual heating of Earth`s atmosphere and surface, a process known as global warming.</p>

<p><b style="color:red;">OZONE DEPLETION : </b>The Ozone Layer protects us from the harmful ultraviolet radiation or UV-B rays of sun. Ozone is a gas in the atmosphere that protects everything living on the Earth from harmful ultraviolet (UV) rays from the Sun. Without the layer of ozone in the atmosphere, it would be very difficult for anything to survive on the surface.</p>

<p>The thinning and depletion of Ozone layer due to emission of CFC caused serious health issues and making surface expose to ultraviolet rays which results in Skin Cancer. </p>

